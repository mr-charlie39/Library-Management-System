package com.lms.pojo;

public class DashboardStats {
	private int totalBooks;
	private int totalUsers;
	private int bookAssigned;
	private int bookReturned;
	public int getTotalBooks() {
		return totalBooks;
	}
	public void setTotalBooks(int totalBooks) {
		this.totalBooks = totalBooks;
	}
	public int getTotalUsers() {
		return totalUsers;
	}
	public void setTotalUsers(int totalUsers) {
		this.totalUsers = totalUsers;
	}
	public int getBookAssigned() {
		return bookAssigned;
	}
	public void setBookAssigned(int bookAssigned) {
		this.bookAssigned = bookAssigned;
	}
	public int getBookReturned() {
		return bookReturned;
	}
	public void setBookReturned(int bookReturned) {
		this.bookReturned = bookReturned;
	}
	
	
	

}
